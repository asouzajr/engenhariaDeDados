# Soma
print("O somatório de 4 e 2: ", 4+2)
# Subtração
print("A diferença entre 4 e 3: ", 4-3)
# Multiplicação
print('A multiplicação entre 5 e 6: ', 5*6)
# Divisão
print("A divisão de 10 por 3: ", 10/4)
# Potência
print('3 elevado ao quadrado: ', 3**2)
print('3 elevado ao quadrado: ', pow(3, 2))
# Resto da divisão
print('O resto da divisão de 21 por 4: ', 21 % 4)
# Operações com tipos de dados ponto flutuante
print("A soma entre 3,1 e 3,4 é: ", 3.1+3.4)
print("A soma entre 3 e 3,4 é: ", 3+3.4)
# Resultado com o tipo de dado ponto flutuante com casas decimais maior que 1
print('5 divido por 3: ', 5/3)
# Resultado com o tipo de dado ponto flutuante com casas decimais maior que 1
print('5 divido por 3: ', 5/3.0)
# Resultado com o tipo de dado ponto flutuante, com uma casa decimal no valor de zero
print('5 divido por 3: ', 5//3.0)
# Resultado com o tipo de dado inteiro, ou seja, considera apenas parte inteira
print('5 divido por 3: ', 5//3)
